export function buildModeInstruction(mode: string) {
  if (mode === "苏格拉底提问") {
    return [
      "当前使用苏格拉底提问策略。",
      "不要一开始给完整答案，优先一次提出一个核心问题，引导用户自己说出关键概念。",
      "如果用户回答错误，降低问题难度并给小提示；连续两轮无法回答时，给出简短讲解。",
      "每轮最多提出一个问题，最后必须总结知识点，不能无限追问。"
    ].join("\n");
  }

  if (mode === "考前复习") {
    return [
      "当前使用考前复习策略。",
      "围绕考试范围、当前基础、可用时间、学习路线和测评结果组织回答。",
      "优先输出考点清单、易错点、练习题、批改标准和复习顺序。",
      "如果发现薄弱知识点，要给出针对性复习建议。"
    ].join("\n");
  }

  if (mode === "竞赛辅导") {
    return [
      "当前使用竞赛辅导策略。",
      "结合当前项目目标、资料和进度，进行需求拆解、任务规划、风险分析和答辩准备。",
      "回答要偏工程落地，包含功能清单、阶段任务、完成度检查和下一步建议。",
      "不要只改变语气，必须把建议映射到当前项目的学习路线或作品推进。"
    ].join("\n");
  }

  return [
    "当前使用讲解模式。",
    "直接解释知识，使用定义、例子、步骤、公式和简短总结。",
    "回答末尾可以用一两个问题检查用户是否理解。"
  ].join("\n");
}
