export function buildDemoResponse(message: string, projectName: string) {
  const normalized = message.trim();

  if (/计划|怎么学|路线/.test(normalized)) {
    return `针对“${projectName}”，我建议把学习过程拆成四步：\n\n1. **建立概念地图**：先确认核心概念、先修知识和常见误区。\n2. **用问题驱动理解**：每个知识点至少回答“是什么、为什么、怎么用”。\n3. **小任务验证**：通过例题、实验或代码把理解变成可检查的结果。\n4. **复盘与迁移**：总结哪些方法能迁移到其他学科。\n\n你可以直接告诉我目前最不清楚的部分，我会从那里开始讲解。`;
  }

  if (/测试|测验|题目|练习/.test(normalized)) {
    return `先来一道诊断题：\n\n**请不用查资料，用自己的话解释这个主题中最核心的因果关系。**\n\n回答后我会从准确性、完整性和可迁移性三个方面给反馈，并自动调整后续难度。`;
  }

  return `我已经把你的问题放到“${projectName}”的学习上下文中。\n\n可以先从三个层次理解：\n\n- **直观层**：它解决什么问题，为什么值得学习；\n- **原理层**：关键变量、因果关系和适用边界；\n- **应用层**：用例题、实验或真实任务验证。\n\n当前是演示模式，因此我先给出学习框架。配置 OpenAI API Key 后，这里会返回真实模型生成的针对性讲解。`;
}
